class Narrowing 
{
	public static void main(String[] args) 
	{
		long l=9897967876l;
		float f=(float)l;
		int i=(int)l;
		byte b=(byte)l;
		double d=(double)l;
		System.out.println(l);
		System.out.println(f);
		System.out.println(i);
		System.out.println(b);
		System.out.println(d);
		
	}
}
