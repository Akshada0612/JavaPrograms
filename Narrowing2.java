class Narrowing2
{
	public static void main(String[] args) 
	{
		
		float f = 3.14f;
		int i=(int)f;
		byte b=(byte)f;
		char c=(char)f;
		long l=(long)f;
	
		System.out.println(f);
		System.out.println(i);
		System.out.println(b);
		System.out.println(c);
		System.out.println(l);
	}
}
